public class PrimitiveDataType {
    public static void main(String[] args) {

        //1 Bit
        boolean amILearning = true;

        //8 Bits or 1 Byte
        byte byteVar = 3;

        //16 Bits or 2 Bytes
        char charVar = 'X';

        //16 Bits or 2 Bytes
        short shortVar = 100;

        //32 Bits or 4 Bytes
        int intVar = 101;

        //64 Bits or 8 Bytes
        long longVar = 123456789L;

        //32 Bits or 4 Bytes
        float floatVar = 1000.01f;

        //64 Bits or 8 Bytes
        double doubleVar = 123456789.01d;


        System.out.println(amILearning);
        System.out.println(byteVar);
        System.out.println(charVar);
        System.out.println(shortVar);
        System.out.println(intVar);
        System.out.println(longVar);
        System.out.println(floatVar);
        System.out.println(doubleVar);
    }
}
